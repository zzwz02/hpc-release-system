def write_golden(name: str, status: int, body: object, note: str = "",
                  expected_status: int | None = None) -> None:
    RESPONSES_DIR.mkdir(parents=True, exist_ok=True)
    scrubbed = scrub(body)
    record = {
        "_golden_name": name,
        "_note": note,
        "status": status,
        "body": scrubbed,
    }
    out_path = RESPONSES_DIR / f"{name}.json"
    out_path.write_text(json.dumps(record, ensure_ascii=False, indent=2), encoding="utf-8")
    # Use caller-supplied expected_status to decide the label; otherwise treat <400 as OK
    exp = expected_status if expected_status is not None else (200 if status < 400 else None)
    ok_mark = "OK" if status == (exp if exp is not None else status) and status < 500 else "ERR"
    print(f"  [{ok_mark}] {name}  HTTP {status}  -> {out_path.name}")


