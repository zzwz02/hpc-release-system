def _make_headers(cookie: str) -> dict[str, str]:
    headers: dict[str, str] = {"Content-Type": "application/json"}
    if cookie:
        headers["Cookie"] = cookie
    return headers


def _client() -> "httpx.Client":
    """Return an httpx client with proxy bypassed for localhost.

    The environment may have http_proxy set (e.g. 127.0.0.1:3576 MCP proxy).
    We always talk directly to the test server on 127.0.0.1 so we must disable
    environment proxy pickup.
    """
    import httpx
    return httpx.Client(trust_env=False, timeout=15)


def _do_get(base: str, path: str, cookie: str, params: str = "") -> tuple[int, object, dict]:
    url = f"{base}{path}"
    if params:
        url = f"{url}?{params}"
    with _client() as client:
        resp = client.get(url, headers=_make_headers(cookie), follow_redirects=False)
    try:
        body = resp.json()
    except Exception:
        body = resp.text
    return resp.status_code, body, dict(resp.headers)


def _do_post(base: str, path: str, cookie: str, payload: dict) -> tuple[int, object, dict, str]:
    """Returns (status, body, headers, new_cookie)."""
    with _client() as client:
        resp = client.post(
            f"{base}{path}",
            content=json.dumps(payload, ensure_ascii=False).encode("utf-8"),
            headers=_make_headers(cookie),
            follow_redirects=False,
        )
    new_cookie = cookie
    set_cookie = resp.headers.get("set-cookie", "")
    if "hpc_session=" in set_cookie:
        for part in set_cookie.split(";"):
            if part.strip().startswith("hpc_session="):
                new_cookie = part.strip()
                break
    try:
        body = resp.json()
    except Exception:
        body = resp.text
    return resp.status_code, body, dict(resp.headers), new_cookie


