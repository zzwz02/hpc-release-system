# ---------------------------------------------------------------------------
# Scrubber — normalises non-deterministic fields so golden diffs are stable
# ---------------------------------------------------------------------------

# Fields whose values are replaced with a stable placeholder
_SCRUB_KEYS = {
    # timestamps / date-times
    "created_at", "updated_at", "submitted_at", "reviewed_at", "delivered_at",
    "generated_at", "released_locked_at", "uploaded_at", "last_visited_at",
    "returned_at",
    # session / token fields
    "token",
    # job ids generated by secrets.token_urlsafe
    "job_id",
    # audit id (auto-increment, varies by run order)
    # "id" is intentionally NOT scrubbed globally — app_id / task_id / release_id
    # are meaningful identity keys we want to keep.
}

# Top-level response keys that may contain large or always-changing bodies
_SCRUB_ARTIFACT_CONTENT_KEYS = {"content"}

_PLACEHOLDER_TS = "SCRUBBED_TIMESTAMP"
_PLACEHOLDER_TOKEN = "SCRUBBED_TOKEN"
_PLACEHOLDER_ID = "SCRUBBED_ID"


def scrub(obj, *, _key: str = "") -> object:
    """Recursively replace non-deterministic fields with stable placeholders.

    Rules:
    - Keys in _SCRUB_KEYS → replaced by a typed placeholder string.
    - The 'ts' key (audit table timestamp) → _PLACEHOLDER_TS.
    - The 'now' / 'generated_at' in QA reports → _PLACEHOLDER_TS.
    - Nested dicts and lists are recursively scrubbed.
    """
    if isinstance(obj, dict):
        out: dict = {}
        for k, v in obj.items():
            if k in _SCRUB_KEYS:
                if "token" in k or k == "job_id":
                    out[k] = _PLACEHOLDER_TOKEN
                else:
                    out[k] = _PLACEHOLDER_TS
            elif k == "ts":
                out[k] = _PLACEHOLDER_TS
            elif k == "password_hash":
                out[k] = "SCRUBBED_HASH"
            else:
                out[k] = scrub(v, _key=k)
        return out
    if isinstance(obj, list):
        return [scrub(item, _key=_key) for item in obj]
    return obj


